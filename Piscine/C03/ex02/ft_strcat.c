char	*ft_strcat(char *dest, char *src)
{
	int b;
	int a;

	a = 0;
	b = 0;
	while (dest[b] != '\0')
		b++;
	dest[b] = src[0];
	while ((dest[b] = src[a]) != '\0')
	{
		b++;
		a++;
	}
	return (dest);
}

#include "libft.h"

int	ft_atoi(const char *str)
{
	int a;
	int b;
	int c;

	a = 0;
	b = 1;
	c = 0;
	while (str[a] == ' ' || (str[a] >= 9 && str[a] <= 13))
		a++;
	if (str[a] == '-')
	{
		b = b * -1;
		a++;
	}
	else if (str[a] == '+')
		a++;
	while (str[a] >= 48 && str[a] <= 57)
	{
		c *= 10;
		c += str[a] - '0';
		a++;
	}
	return (c * b);
}
